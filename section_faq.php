							Each property has its own NFTs representing the ownership interests in that property. In order to purchase NFTs for a property, you must first select an available property <a href="/property" class="underline text-light-blue">here</a>. Once you have selected a property to invest in, simply click the Invest button for that property and you will be provided with step-by-step directions on how to purchase your NFTs.

